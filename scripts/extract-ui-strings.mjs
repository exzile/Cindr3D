import fs from 'node:fs';
import path from 'node:path';
import ts from 'typescript';

const repoRoot = process.cwd();
const sourceRoots = ['src/app', 'src/components', 'src/pwa'].map((dir) => path.join(repoRoot, dir));
const outputPath = path.join(repoRoot, 'src/i18n/locales/en.generated.ts');
const ignoredText = new Set(['', '{', '}', 'true', 'false']);

function walk(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) return walk(full);
    return /\.(tsx|ts)$/.test(entry.name) && !entry.name.endsWith('.test.ts') ? [full] : [];
  });
}

function cleanText(value) {
  return value
    .replace(/\s+/g, ' ')
    .replace(/[“”]/g, '"')
    .replace(/[‘’]/g, "'")
    .trim();
}

function slug(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '.')
    .replace(/^\.+|\.+$/g, '')
    .slice(0, 48) || 'text';
}

function hash(value) {
  let h = 5381;
  for (let i = 0; i < value.length; i += 1) h = ((h << 5) + h) ^ value.charCodeAt(i);
  return (h >>> 0).toString(36);
}

function add(strings, value) {
  const text = cleanText(value);
  if (ignoredText.has(text) || text.length < 2) return;
  if (/^[{}()[\].,;:+*/\\|<>=!?-]+$/.test(text)) return;
  strings.add(text);
}

function extractFromFile(filePath, strings) {
  const source = ts.createSourceFile(filePath, fs.readFileSync(filePath, 'utf8'), ts.ScriptTarget.Latest, true, ts.ScriptKind.TSX);
  const visit = (node) => {
    if (ts.isJsxText(node)) add(strings, node.getText(source));
    if (ts.isJsxAttribute(node) && node.initializer) {
      const attrName = node.name.getText(source);
      if (['title', 'aria-label', 'placeholder', 'alt'].includes(attrName) && ts.isStringLiteral(node.initializer)) {
        add(strings, node.initializer.text);
      }
    }
    ts.forEachChild(node, visit);
  };
  visit(source);
}

const strings = new Set();
sourceRoots.flatMap(walk).forEach((file) => extractFromFile(file, strings));

const entries = [...strings].sort((a, b) => a.localeCompare(b)).map((value) => {
  const key = `ui.${slug(value)}.${hash(value)}`;
  return `  ${JSON.stringify(key)}: ${JSON.stringify(value)},`;
});

const content = `// Generated by scripts/extract-ui-strings.mjs. Do not edit by hand.\nexport const enGenerated = {\n${entries.join('\n')}\n} as const;\n`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, content);
console.log(`Extracted ${entries.length} UI strings to ${path.relative(repoRoot, outputPath)}`);
